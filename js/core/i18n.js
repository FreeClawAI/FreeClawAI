const I18n = {
    _lang: 'en',
    _data: {},
    init() {
        var navLang = navigator.language || navigator.userLanguage || '';
        this._lang = navLang.startsWith('zh') ? 'zh' : 'en';
        this._data = this._lang === 'zh' ? I18n_ZH : I18n_EN;
    },
    t(key, ...args) {
        var str = this._data[key] || key;
        for (var i = 0; i < args.length; i++) {
            str = str.replace('{' + i + '}', args[i]);
        }
        return str;
    }
};